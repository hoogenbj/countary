package hoogenbj.countary.model;

import javafx.scene.paint.Color;

import java.util.List;
import java.util.Objects;

public record Category(Long id, String name, String bgColor, Kind kind, Category parent) {
    public static String DEFAULT_BACKGROUND_COLOR = "grey";

    public Color getBgColor() {
        if (bgColor.isBlank())
            return Color.web(DEFAULT_BACKGROUND_COLOR);
        else
            return Color.web(bgColor);
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        Category category = (Category) o;
        return id.equals(category.id);
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }
}
